    private void btnEjecutarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEjecutarMouseClicked
        
        Double costo = Double.parseDouble(producto.getText());
        Double pago =  Double.parseDouble(pagar.getText());
        
        if(costo > pago){
        JOptionPane.showMessageDialog(rootPane, "No cuenta con dinero sufiiente para pagar el producto", "ATENCIÃ“N", JOptionPane.WARNING_MESSAGE);
        }else{
        
        Double cambio = pago - costo;
         DecimalFormat df = new DecimalFormat("#.00");
        pizarra.setText("Su cambio es de: " + df.format(cambio) + " pesos mexicanos");
        }
    }//GEN-LAST:event_btnEjecutarMouseClicked

